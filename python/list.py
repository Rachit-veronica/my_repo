# creating a list using [] 
a=[1,2,4,6,7,5,23,43]

# printing a list via print() function 
print(a)

# access using index using a[0] a[3] a[2] 
print(a[3])

# change the value of list using index 
a[2] =878
print(a)

# we can create the list of item of different types 
b= [32,'shashank',99 ,'gupta', False , 9.2]
print(b)

# list slicing  
print(a[2:5])